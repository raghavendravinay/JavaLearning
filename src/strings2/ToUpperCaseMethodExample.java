package strings2;

public class ToUpperCaseMethodExample {

	public static void main(String[] args) {
		String s = "Vinay";
		// toUpperCase() - Lower case letters in the given String will be converted into
		// Upper case
		System.out.println(s.toUpperCase());// VINAY
		String s2 = "raghavendra";
		System.out.println(s2.toUpperCase());// RAGHAVENDRA
	}
}
