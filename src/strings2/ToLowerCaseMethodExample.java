package strings2;

public class ToLowerCaseMethodExample {

	public static void main(String[] args) {
		String s = "Raghavendra Vinay M";
		// toLowerCase() - upper case letters in the given string will be converted into
		// lower case
		System.out.println(s.toLowerCase()); // raghavendra vinay m
		String s2 = "VINAY";
		System.out.println(s2.toLowerCase());// vinay
	}
}
