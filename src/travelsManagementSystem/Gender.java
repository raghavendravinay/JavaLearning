package travelsManagementSystem;

public class Gender {

	private String gender;

	public Gender(String gender) {
		super();
		this.gender = gender;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String toString() {
		return "Gender=" + gender;
	}

}
